public class noweXY {
    public int nX;
    public int nY;

    noweXY(int xx,int yy){
        nX=xx;
        nY=yy;
    }

    public void setnX(int nX) {
        this.nX = nX;
    }

    public void setnY(int nY) {
        this.nY = nY;
    }

    public int getnX() {
        return nX;
    }

    public int getnY() {
        return nY;
    }

}
